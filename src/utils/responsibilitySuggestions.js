// Get suggestions for a specific role
export const getResponsibilitiesForRole = (role) => {
  // Direct match
  if (responsibilitySuggestions[role]) {
    return responsibilitySuggestions[role];
  }
  
  // Fuzzy match for similar roles
  const lowerRole = role.toLowerCase();
  for (const [key, value] of Object.entries(responsibilitySuggestions)) {
    if (key.toLowerCase().includes(lowerRole) || lowerRole.includes(key.toLowerCase())) {
      return value;
    }
  }
  
  // Default suggestions
  return [
    'Contributed to team objectives and project deliverables',
    'Collaborated with cross-functional teams',
    'Participated in agile development processes',
    'Maintained high quality standards',
    'Communicated effectively with stakeholders'
  ];
};